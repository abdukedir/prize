export function translate(language: Language, key: string, values?: Record<string, string | number>) {
  const template = dictionaries[language]?.[key] ?? dictionaries.en[key] ?? key;
  if (!values) return template;
  return Object.entries(values).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}

export function useI18n() {
  const [language, setLanguage] = useState<Language>("en");

  useEffect(() => {
    api<{ settings: { language: Language } }>("/api/settings")
      .then((data) => setLanguage(data.settings.language))
      .catch(() => setLanguage("en"));

    const onLanguageChange = (event: Event) => {
      const next = (event as CustomEvent<Language>).detail;
      if (next) setLanguage(next);
    };

    window.addEventListener("languagechange", onLanguageChange);
    return () => window.removeEventListener("languagechange", onLanguageChange);
  }, []);

  const t = useCallback((key: string, values?: Record<string, string | number>) => translate(language, key, values), [language]);

  return {
    language,
    t
  };
}

export function statusKey(status: string) {
  const normalized = status.toLowerCase();
  return `status${normalized.charAt(0).toUpperCase()}${normalized.slice(1)}`;
}
